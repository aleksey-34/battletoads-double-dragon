const fallbackLanguage: UILanguage = 'en';

const detectInitialLanguage = (): UILanguage => {
  const saved = String(localStorage.getItem(STORAGE_KEY) || '').toLowerCase();
  if (saved === 'ru' || saved === 'en' || saved === 'tr') {
    return saved;
  }

  const browserLang = String(navigator.language || '').toLowerCase();
  if (browserLang.startsWith('ru')) return 'ru';
  if (browserLang.startsWith('tr')) return 'tr';
  return 'en';
};

const I18nContext = createContext<I18nContextValue>({
  language: fallbackLanguage,
  setLanguage: () => {},
  t: (key, fallback, params) => {
    let text = fallback || key;
    if (params) {
      for (const [name, value] of Object.entries(params)) {
        text = text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value));
      }
    }
    return text;
  },
});

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState<UILanguage>(() => detectInitialLanguage());

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.body.classList.add('battletoads-theme');
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (key: string, fallback?: string, params?: TranslateParams): string => {
      const dict = dictionaries[language] || {};
      const fallbackDict = dictionaries[fallbackLanguage] || {};

      let text = dict[key] || fallbackDict[key] || fallback || key;

      if (params) {
        for (const [name, value] of Object.entries(params)) {
          text = text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value));
        }
      }

      return text;
    };

    return {
      language,
      setLanguage,
      t,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = (): I18nContextValue => useContext(I18nContext);
